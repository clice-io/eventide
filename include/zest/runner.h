#pragma once

#include <functional>
#include <source_location>
#include <string>
#include <vector>

namespace zest {

enum class TestState {
    Passed,
    Skipped,
    Failed,
    Fatal,
};

struct TestAttrs {
    bool skip = false;
    bool focus = false;
};

struct TestCase {
    std::string name;
    std::string path;
    std::size_t line;
    TestAttrs attrs;
    std::function<TestState()> test;
};

struct TestSuite {
    std::string name;
    std::vector<TestCase> (*cases)();
};

class Runner {
public:
    static Runner& instance();

    void add_suite(std::string_view suite, std::vector<TestCase> (*cases)());

    int run_tests(std::string_view filter);

private:
    std::vector<TestSuite> suites;
};

}  // namespace zest
